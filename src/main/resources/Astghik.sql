/*
CREATE DATABASE ASTGHIK;
*/

DROP TABLE IF EXISTS ASTGHIK.GROUP;
DROP TABLE IF EXISTS ASTGHIK.TEACHER;
DROP TABLE IF EXISTS ASTGHIK.ROOM;
DROP TABLE IF EXISTS ASTGHIK.LESSON;
DROP TABLE IF EXISTS ASTGHIK.LESSON_TIME;
DROP TABLE IF EXISTS ASTGHIK.CONFLICT;

CREATE TABLE ASTGHIK.GROUP (
  ID   INT NOT NULL,
  NAME VARCHAR(255),
  PRIMARY KEY (`ID`)
);


CREATE TABLE ASTGHIK.TEACHER (
  ID   INT NOT NULL,
  NAME VARCHAR(255),
  PRIMARY KEY (`ID`)
);

CREATE TABLE ASTGHIK.ROOM (
  ID   INT NOT NULL,
  NAME VARCHAR(255),
  PRIMARY KEY (`ID`)
);

CREATE TABLE ASTGHIK.LESSON (
  ID   INT NOT NULL,
  NAME VARCHAR(255),
  PRIMARY KEY (`ID`)
);

CREATE TABLE ASTGHIK.LESSON_TIME (
  ID   INT NOT NULL,
  NAME VARCHAR(255),
  PRIMARY KEY (`ID`)
);

CREATE TABLE ASTGHIK.CONFLICT (
  ID   INT NOT NULL,
  NAME VARCHAR(255),
  PRIMARY KEY (`ID`)
);


CREATE TABLE ASTGHIK.SCHEDULE (
  ID             INT AUTO_INCREMENT NOT NULL,
  GROUP_ID       INT                NOT NULL,
  TEACHER_ID     INT                NOT NULL,
  ROOM_ID        INT                NOT NULL,
  LESSON_ID      INT                NOT NULL,
  LESSON_TIME_ID INT                NOT NULL,
  CONFLICT_ID    INT                NOT NULL,
  PRIMARY KEY (`ID`),
  CONSTRAINT as_group FOREIGN KEY (GROUP_ID) REFERENCES ASTGHIK.GROUP (ID),
  CONSTRAINT as_teacher FOREIGN KEY (TEACHER_ID) REFERENCES ASTGHIK.TEACHER (ID),
  CONSTRAINT as_room FOREIGN KEY (ROOM_ID) REFERENCES ASTGHIK.ROOM (ID),
  CONSTRAINT as_lesson FOREIGN KEY (LESSON_ID) REFERENCES ASTGHIK.LESSON (ID),
  CONSTRAINT as_lessonTime FOREIGN KEY (LESSON_TIME_ID) REFERENCES ASTGHIK.LESSON_TIME (ID),
  CONSTRAINT as_conflict FOREIGN KEY (CONFLICT_ID) REFERENCES ASTGHIK.CONFLICT (ID)
);


